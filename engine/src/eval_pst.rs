use crate::board::Board;
use crate::pieces::{Color, Piece};

pub struct PST;

impl PST {
    pub fn evaluate(board: &Board) -> i32 {
        let mut score = 0;

        for color in [Color::White, Color::Black] {
            for piece in Piece::ALL {
                let bb = board.pieces[board.index(color, piece)];

                let mut b = bb;
                while b != 0 {
                    let sq = b.trailing_zeros() as usize;
                    b &= b - 1;

                    let val = PST::value(piece, color, sq);
                    score += if color == Color::White { val } else { -val };
                }
            }
        }

        score
    }

    fn value(piece: Piece, color: Color, sq: usize) -> i32 {
        let idx = if color == Color::White {
            sq
        } else {
            63 - sq // mirror for black
        };

        match piece {
            Piece::Pawn => PAWN_PST[idx],
            Piece::Knight => KNIGHT_PST[idx],
            Piece::Bishop => BISHOP_PST[idx],
            Piece::Rook => ROOK_PST[idx],
            Piece::Queen => QUEEN_PST[idx],
            Piece::King => KING_PST_MID[idx], // later: KING_PST_END for endgame
        }
    }
}

